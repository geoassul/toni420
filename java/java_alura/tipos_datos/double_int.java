public class double_int {

    public static void main(String[] args){

        float puntoFlotante = 3.14f; // es un dato decimal de 32bits

        double salario = 1270.50; //se usa double para datos decimales
        int valor = (int) salario; // se llamas CAST a la accion de convertir datos
        System.out.println(valor);

        double valor1 = 0.2; // es un dato decimal de 64bits (- , +)
        double valor2 = 0.1;
        double total = valor1 + valor2;

        System.out.println(total);
    }
 // hay datoa de un bit : boolean
 // datos de 8bits = byte
 // dato de escritura = char o string
 // int : es un dato de 32 bits 
}
