#include "p18f4550.inc"
#include "config.inc"

CONTADOR 	EQU		0X10
NUM_CARGA	EQU		.60

	ORG		X0000
	GOTO	SETUP
	ORG		0X0020
	
SETUP:
	MOVLW	0X62
	MOVWF	OSCCON	
	MOVLW	0X0F	
	MOVWF	ADCON1
	CLRF	TRISD
	
LOOP:
	MOVLW	NUM_CARGA
	MOVWF	CONTADOR
	
VISUALIZAR:
	CALL	VerCONTADOR
	DECFSZ	CONTADOR,F
	GOTO	VISUALIZAR
	CALL	VerCONTADOR
	CALL	retardo_500ms
	GOTO	LOOP
	
	
VerCONTADOR:
	MOVF	CONTADOR,W
	CALL	BIN_A_BCD
	MOVWF	LATD
	CALL	retardo_500ms
	RETURN
	
#include "BIN_BCD.INC"
#include "retardos.inc"

	end

